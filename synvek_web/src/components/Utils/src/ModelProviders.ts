export interface ModelFile {
  repoName: string
  repoFile: string
}

export interface ModelRepo {
  repoName: string
}

export interface ModelOption {
  name: string
  fileSize: string
  repos: ModelRepo[]
  files: ModelFile[]
}

export type ModelCategory = 'text-to-text' | 'image-to-text' | 'speech-to-text' | 'video-to-text' | 'text-to-image' | 'text-to-speech'
export type ModelType = 'run' | 'plain' | 'vision-plain' | 'diffusion' | 'speech' | 'uqff' | 'gguf' | 'stable-diffusion'
export type BackendType = 'default' | 'llama_cpp' | 'stable_diffusion_cpp' | 'whisper_cpp'
export type AccelerationType = 'cpu' | 'cuda' | 'cuda_legacy' | 'metal' | 'cpu-mkl' | 'cpu-accelerate' | 'vulkan' | 'opencl' | 'webgpu'

export interface ModelProviderStatus {
  name: string
  modelId: string
  modelCreator?: string
  modelSource?: string
  downloading?: boolean
  downloaded?: boolean
  accessToken?: string
  mirror?: string
  repos?: ModelRepo[]
  files?: ModelFile[]
}

